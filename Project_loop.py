#Check if a Number is Even or Odd

a=int(input("Enter your number:"))
if (a%2==0):
    print(a," is an even number")
else:
    print(a,' is an odd number')



#Sum of Integers from 1 to 50 Using a Loop

sum=0
n=(range(1,51))
for i in n:
   sum=i+sum
print('Sum of Integers from 1 to 50=',sum)



